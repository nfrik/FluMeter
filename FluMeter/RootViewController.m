// Based on the magnitude of the earthquake, return an image indicating its seismic strength.
-(UIImage *)imageForValue:(CGFloat)value{
	if (value >= 80.0) {
		return [UIImage imageNamed:@"ActivitySevere.png"];
	}
	if (value >= 60.0) {
		return [UIImage imageNamed:@"ActivityHigh.png"];
	}
	if (value >= 40.0) {
		return [UIImage imageNamed:@"ActivityElevated.png"];
	}
	if (value >= 20.0) {
		return [UIImage imageNamed:@"ActivityGuarded.png"];
	}
    if (value >= 0.0){
	    return [UIImage imageNamed:@"ActivityLow.png"];
    }
    return [UIImage imageNamed:@"ActivityNoInformationAvailable.png"];
}

