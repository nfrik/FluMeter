-(IBAction)sliderTemperatureChanged:(id)sender{
	UISlider *slider = (UISlider *) sender;
	int progressAsInt =(int)(slider.value + 0.5f);
    if (progressAsInt<0) {
        NSString *newText =[[NSString alloc]
                            initWithFormat:@"%@",@"Use GPS"];
        self.sliderTemperatureLabel.text = newText;
        [newText release];
    }
    else{
        NSString *newText =[[NSString alloc]
                            initWithFormat:@"%d °C %d °F",progressAsInt,[self celsiusToFarenheit:progressAsInt]];
        self.sliderTemperatureLabel.text = newText;
        [newText release];
    }
}

-(IBAction)sliderHumidityChanged:(id)sender{
	UISlider *slider = (UISlider *) sender;
	int progressAsInt =(int)(slider.value + 0.5f);
    if (progressAsInt<10) {
        NSString *newText =[[NSString alloc]
                            initWithFormat:@"%@",@"Use GPS"];
